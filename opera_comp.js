let valor1, valor2;

valor1 = 20;
valor2 = 10;

document.write(valor1 > valor2);// mayor que
document.write('<br>')
document.write(valor1 < valor2);//menor que
document.write('<br>')
document.write(valor1 == valor2);//comparando
document.write('<br>');
document.write(valor1 != valor2)//diferente
document.write('<br>')
document.write(valor1 <= valor2)//menor o igual
document.write('<br>')
document.write(valor1 >= valor2)// mayor o igual que
